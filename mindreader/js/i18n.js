    setLanguage(lang) {
        this.currentLang = lang;
        document.documentElement.lang = lang;
        document.documentElement.dir = this.rtlLanguages.includes(lang) ? 'rtl' : 'ltr';
        localStorage.setItem('mindreader-lang', lang);
    },

    t(key) {
        const lang = this.translations[this.currentLang];
        return (lang && lang[key]) || this.translations.en[key] || key;
    },

    init() {
        const saved = localStorage.getItem('mindreader-lang');
        if (saved && this.translations[saved]) {
            this.setLanguage(saved);
        }
    }
